#include <stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////
//
// Function Name:   Accept
// Input:           Integer
// Output:          None
// Description:     This function accepts a number from user and prints that number of times * on screen
// Author:          Rushikesh Vinod Gunthe
// Date:            24/10/2025
//
/////////////////////////////////////////////////////////////////////////////////////////

void Accept(int iNo)
{
    int iCnt = 0;
    for (iCnt = 1; iCnt <= iNo; iCnt++)
    {
        printf("*");
    }
}

int main()
{
    int iValue = 0;

    printf("Enter number:\n");
    scanf("%d", &iValue);

    Accept(iValue);

    return 0;
}
